public class Person {
	/**
	 * Name of a person
	 */
	private String name;
	/**
	 * Age of a person
	 */
	private float age;
	/**
	 * Corresponding getters and setters
	 * @return
	 */
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getAge() {
		return age;
	}
	public void setAge(float age) {
		this.age = age;
	}
}
